#to replace particular element in list
numbers = [1,1,1,1,1]
numbers[2] = 2
print(numbers)

#to join multiple list
number2 = [3,3,3,3,3]
print(numbers + number2)

#to print the list element multiple times
print(numbers*3)

#to know if particular element is present in the list
fruits = ["Orange", "Apple", "Mange"]
#it will return True or False (Case sensitive)
print ("Apple" in fruits)